/* Each character must be drawn page by page from memory, so this must be the
 * data byte order! Also, since 8 pixels (vertical) must be drawn each time,
 * the font height must be a multiple of 8.
 */
static struct _font _fonts[] = {
	{
		.data = _font_6x8,
		.width = 6,
		.height = 8,
		.first_char = ' ',
		.chars = sizeof(_font_6x8) / 6,
	},
	{
		.data = _font_8x16,
		.width = 8,
		.height = 16,
		.first_char = 0,
		.chars = sizeof(_font_8x16) / 8,
	},
	{
		.data = _numbers_16x32,
		.width = 16,
		.height = 32,
		.first_char = '0',
		.chars = sizeof(_numbers_16x32) / 16,
	},
	{
		.data = _symbol_spo2_64x32,
		.width = 16,
		.height = 32,
		.first_char = 1,
		.chars = 4,
	},
};

static struct _font *_font = &_fonts[0];
static int _col = 0;
static int _line = 0;
static int _offx = 0;
static int _offy8 = 0;


static void _nextchar(void)
{
	_col++;
	if (_col > 128 / _font->width - 1) {
		_line++;
		_col = 0;
	}
	if (_line > 64 / _font->height - 1)
		_line = 0;
}

bool ssd1306_putc_xy(uint8_t c, uint8_t col, uint8_t line)
{
	static uint8_t data[64] /* largest font character is (16x32) */;
	bool err = true;
	size_t char_bytes = (int)_font->width * (int)_font->height / 8;
	size_t pos = char_bytes * (c - _font->first_char);
	uint8_t wcol = _offx + _font->width * col; /* segment (1 px each) */
	uint8_t wline = _offy8 + _font->height * line / 8; /* page (8 px each) */
	uint8_t wline_fill = _font->height / 8; /* pages to fill */

	/* check for character out of bounds */
	assert(!(c < _font->chars + _font->first_char));
	memcpy_P(data, &_font->data[pos], char_bytes);

	for (uint8_t page = 0; page < wline_fill; page++) {
		assert(ssd1306_setpos(wcol, wline + page));
		assert(ssd1306_fill(data + page * _font->width,
				    _font->width));
	}
	err = false;
exit:
	return err;
}

bool ssd1306_font_setfont(enum ssd1306_font font)
{
	bool err = true;

	if (font >= 0 && font < ssd1306_font_max) {

		_font = &_fonts[font];
		_col = 0;
		_line = 0;
		_offx = 0;
		_offy8 = 0;
		err = false;
	}
exit:
	return err;
}

bool ssd1306_font_setpos(uint8_t col, uint8_t line)
{
	_col = col;
	_line = line;
	return 0;
}

bool ssd1306_font_setoffset(uint8_t x, uint8_t y)
{
	_offx = x;
	_offy8 = y / 8;
	return 0;
}

bool ssd1306_font_putc(uint8_t c)
{
	bool err = true;
	assert(ssd1306_putc_xy(c, _col, _line));
	_nextchar();
	err = false;
exit:
	return err;
}

bool ssd1306_font_printf_P(const char *fmt_P, ...)
{
	bool err = true;
	static char buf[33];
	va_list ap;

	va_start(ap, fmt_P);
	vsnprintf_P(buf, sizeof(buf), fmt_P, ap);
	va_end(ap);
	for (size_t i = 0; buf[i] && i < sizeof(buf); i++)
		assert(ssd1306_font_putc(buf[i]));
	err = false;
exit:
	return err;
}